
	OUTPUT 2500AD
.ORG 2000H
DATA SEGMENT
STRING DB "HELLO"
CHAR DB 'L'
COUNT EQU 05H
RESULT DB 00H
DATA ENDS
CODE SEGMENT
	ASSUME CS:CODE,DS:DATA,ES:DATA
	MOV AX,DATA
	MOV DS,AX
	MOV ES,AX
	MOV CL,COUNT
	CLD
	LEA DI,STRING
	MOV AL,CHAR
L2:	SCASB
	JNZ L1
	MOV RESULT,01H
	JMP L3
L1:	MOV RESULT,00H
	INC DI
	DEC CL
	JNZ L2
	MOV RESULT,00H
L3:	HLT
CODE ENDS
END
