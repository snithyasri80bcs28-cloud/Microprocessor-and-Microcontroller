		OUTPUT 2500AD
.ORG 2000H
DATA SEGMENT
STR1 DB 'HELLO$'
STR2 DB 'HAPPY$'
OUT1 DB 00H,00H
DATA ENDS
CODE SEGMENT
	ASSUME CS:CODE,DS:DATA
	MOV AX,DATA
	MOV DS,AX
	LEA SI,STR1
	LEA DI,STR2
LOOP1:
	MOV AL,[SI]
	MOV BL,[DI]
	CMP AL,BL
	JNE NOT_EQ
	CMP AL,'$'
	JE IS_EQ
	INC SI
	INC DI
	JMP LOOP1
IS_EQ:
	MOV AL,01H
	MOV OUT1+1,AL
	JMP END_PROG
NOT_EQ:
	MOV AL,00H
	MOV OUT1+1,AL
END_PROG:
	HLT
CODE ENDS
END

